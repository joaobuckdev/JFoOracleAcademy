public class JFoSection2 {
  public static void main(String args[]) {
    /* Problema 1: Arte ASCII Simples */
    System.out.println("    ####    ");
    System.out.println("   #    #   ");
    System.out.println("  # #  # #  ");
    System.out.println("  #      #  ");
    System.out.println("  # #  # #  ");
    System.out.println("  #  ##  #  ");
    System.out.println("   #    #   ");
    System.out.println("    ####    ");

    /* Problema 2: Arte ASCII Original */
    System.out.println("\n\nASCII ORIGINAL");
    System.out.println("☆┌─┐　─┐☆");
    System.out.println("　│▒│ /▒/");
    System.out.println("　│▒│/▒/");
    System.out.println("　│▒ /▒/─┬─┐");
    System.out.println("　│▒│▒|▒│▒│");
    System.out.println("┌┴─┴─┐-┘─┘");
    System.out.println("│▒┌──┘▒▒▒│");
    System.out.println("└┐▒▒▒▒▒▒┌┘");
    System.out.println("　└┐▒▒▒▒┌");
    /* O ASCII retrata o sinal de V (sinal da paz) */
  }
}